package constructor;

public class ConstructorDocs {
	
	/*
	 * ------------ Constructor ---------------
	 * # use to create object in java.
	 * 
	 * # special type of method which has same name 
	 *   as class name is known as constructor.
	 *   
	 * # no return type
	 * 
	 * # if there is no any user defined constructor in class
	 *    then java provide default constructor. 
	 *
	 * 
	 *    #Type :
	 *     a> Default Constructor :
	 *     		# use to create object and set default values to it.
	 *      
	 *          syntax :
	 *            class_name(){
	 *                //set default values to object
	 *            }
	 *       
	 *     b> Parameterized Constructor :
	 *       # use to create object and set dynamic values to it.
	 *       
	 *      syntax :
	 *        
	 *          class_name(arg1,arg2,...,argn){
	 *             
	 *                //set dynamic values to object
	 *          }
	 * 
	 */

}
